    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int i = jTable2.getSelectedRow();
        if (i == -1){
            return;
        }
        String id = (String)model.getValueAt(i,0);
        String nama = txNamaBarang.getText();
        String harga = txHargaBarang.getText();
        String merek = txMerek.getText();
        String stok = txStok.getText();
        String deskripsi = txDeskripsi.getText();
        
        try {
            Connection c = koneksi.getKoneksiAdmin();
            String sql = "UPDATE databarang SET Nama_Barang = ?, Harga_Barang = ?, Merek = ?, Stok = ?, Deskripsi = ? WHERE ID = ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, nama);
            p.setString(2, harga);
            p.setString(3, merek);
            p.setString(4, stok);
            p.setString(5, deskripsi);
            p.setString(6, id);
            
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            btnSimpan.setEnabled(true);
            btnEdit.setEnabled(false);
            btnHapus.setEnabled(false);
            btnBatal.setEnabled(false);
            clear();
        } catch (Exception e) {
            System.out.println("Update Error");
        } finally {
            loadData();
            autonumber();
        }
    }//GEN-LAST:event_btnEditActionPerformed

