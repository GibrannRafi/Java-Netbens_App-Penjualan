    public void loadData(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            Connection c = koneksi.getKoneksiAdmin();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM databarang";
            ResultSet r = s.executeQuery(sql);
            
            while(r.next()){
                Object[] o = new Object[6];
                    o [0] = r.getString("ID");
                    o [1] = r.getString("Nama_Barang");
                    o [2] = r.getString("Harga_Barang");
                    o [3] = r. getString("Merek");
                    o [4] = r.getString("Stok");
                    o [5] = r.getString("Deskripsi");
                    model.addRow(o);
                    
            }
            r.close();
            s.close();
        } catch (Exception e){
            System.out.println("Terjadi Kesalahan load data");
        }
    }
   
    
   
