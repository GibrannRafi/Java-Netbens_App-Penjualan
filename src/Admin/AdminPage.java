    private void autonumber(){
        try{
            Connection c = koneksi.getKoneksiAdmin();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM databarang ORDER BY ID DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()){
                String ID = r.getString("id").substring(2);
                String BR = "" +(Integer.parseInt(ID)+1);
                String Nol = "";
                
                if (BR.length()==1)
                {Nol = "00";}
                if (BR.length()==2)
                {Nol = "0";}
                if (BR.length()==3)
                {Nol = "";}
                
                txID.setText(Nol + BR);
            } else {
                txID.setText("001");
            }
            r.close();
            s.close();      
        } catch (Exception e) {
            System.out.println("AutoNumber Error");
        }
    }
        
    
